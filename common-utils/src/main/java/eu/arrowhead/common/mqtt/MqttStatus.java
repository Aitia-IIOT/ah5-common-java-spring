package eu.arrowhead.common.mqtt;

public enum MqttStatus {

	OK(200), CREATED(201), NO_CONTENT(204), INTERNAL_SERVER_ERROR(500);

	//=================================================================================================
	// members

	private final int value;

	//=================================================================================================
	// methods

	//-------------------------------------------------------------------------------------------------
	private MqttStatus(final int value) {
		this.value = value;
	}

	//-------------------------------------------------------------------------------------------------
	public int value() {
		return value;
	}
}
