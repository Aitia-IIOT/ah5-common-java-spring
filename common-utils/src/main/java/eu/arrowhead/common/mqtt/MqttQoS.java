package eu.arrowhead.common.mqtt;

public enum MqttQoS {

	AT_MOST_ONCE(0), AT_LEAST_ONCE(1), EXACTLY_ONCE(2);

	//=================================================================================================
	// members

	private final int value;

	//=================================================================================================
	// methods

	//-------------------------------------------------------------------------------------------------
	private MqttQoS(final int value) {
		this.value = value;
	}

	//-------------------------------------------------------------------------------------------------
	public int value() {
		return value;
	}

}
